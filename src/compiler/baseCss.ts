function typographyBridgeCss(tokens: StyleflowTokensFile): string {
  const model = new TokenModel(tokens);
  const declarations: string[] = [];

  for (const style of typographyTextStyles(tokens)) {
    const prefix = cssSlug(style.tokenPrefix);
    const isHeading = style.group === "heading";
    declarations.push(`  --typography-${prefix}-family: var(--sf-font-${isHeading ? "heading" : "body"}, ${typographyCssValue(model, `${style.tokenPrefix}/family`, "Inter, Arial, sans-serif")});`);
    declarations.push(`  --typography-${prefix}-weight: var(--sf-weight-${isHeading ? "heading" : "body"}, ${typographyCssValue(model, `${style.tokenPrefix}/weight`, "400")});`);
    declarations.push(`  --typography-${prefix}-line-height: var(--sf-leading-${isHeading ? "heading" : "body"}, ${typographyCssValue(model, `${style.tokenPrefix}/line-height`, "1.5")});`);
  }

  return `:root {\n${declarations.join("\n")}\n}`;
}

