import { COLLECTION_NAMES } from "../contracts.js";
import type { StyleflowTokensFile } from "../format/index.js";
import { typographyTextStyles } from "./contractOutput.js";
import { cssSlug, TokenModel } from "./model.js";

export interface BaseCssOptions {
  utilities?: boolean;
  typography?: boolean;
}

export function generateBaseCss(tokens: StyleflowTokensFile, options: BaseCssOptions = {}): string {
  const chunks = ["/* Generated by StyleFlow. Base utilities and typography defaults. */"];
  if (options.utilities !== false) {
    chunks.push(utilityCss());
  }
  if (options.typography !== false) {
    chunks.push(typographyBridgeCss(tokens), typographyBaseCss());
  }
  return `${chunks.filter(Boolean).join("\n\n")}\n`;
}

export function generateTypographyOverrideCss(tokens: StyleflowTokensFile): string {
  const model = new TokenModel(tokens);
  const value = (name: string, fallback: string) => typographyCssValue(model, name, fallback);
  return `/* StyleFlow typography overrides.
 * This file is created once and is not overwritten by styleflow build.
 * Import it after .styleflow/runtime.css and .styleflow/base.css.
 */
:root {
  --sf-font-body: ${value("body/default/body-md/family", "Inter, Arial, sans-serif")};
  --sf-font-heading: ${value("heading/default/heading-2/family", "Inter, Arial, sans-serif")};
  --sf-weight-body: ${value("body/default/body-md/weight", "400")};
  --sf-weight-heading: ${value("heading/default/heading-2/weight", "700")};
  --sf-leading-body: ${value("body/default/body-md/line-height", "24px")};
  --sf-leading-heading: ${value("heading/default/heading-2/line-height", "40px")};
}
`;
}

