function typographyCssValue(model: TokenModel, name: string, fallback: string): string {
  const number = model.resolveNumber(COLLECTION_NAMES.typography, name, "Base");
  if (number !== undefined) {
    return name.endsWith("/weight") ? trimNumber(number) : `${trimNumber(number)}px`;
  }
  const string = model.resolveString(COLLECTION_NAMES.typography, name, "Base");
  return string ? JSON.stringify(string) : fallback;
}

function trimNumber(value: number): string {
  return Number(value.toFixed(4)).toString();
}
